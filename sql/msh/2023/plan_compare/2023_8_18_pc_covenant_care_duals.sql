CREATE TEMP TABLE _milliman_logic_calculations_step_1 AS

select mr.id,
       dual_status_code                                                   as dual_level,
       dual_type,
       cost_share_code                                                    as lis_level,
       case when cost_share_code is null then 'Unknown' else lis_type end as lis_type,

       case
           when producttypeid = '3' then 'N'
           when producttypeid = '2' then 'Y'
           when producttypeid = '1' and cms.rx_coverage = 1 then 'Y'
           when producttypeid = '1' and cms.rx_coverage = 2 then 'N'
           else 'N' end                                                   as rx_coverage,
       case
           --    when producttypeid = '3' then (estimatedtotaloopc::float - estimatedmedoopc::float)
           when producttypeid = '3' then 0::float
           else mr.expectedpremium::float
           end                                                            as expectedpremium,
       round((case
           --   when producttypeid = '3' and (estimatedtotaloopc::float - estimatedmedoopc::float) = 0 then
                  when producttypeid = '3' then
                      amp.med_supp_monthly_premium::float
           --   when producttypeid = '3' then (estimatedtotaloopc::float - estimatedmedoopc::float) / 12
                  when producttypeid = '3' and estimatedrxoopc != 'Not Applicable' then (estimatedrxoopc::float) / 12
                  when producttypeid = '2' or producttypeid = '1' then mr.expectedpremium ::float / 12
                  else '0.00'::float end)::numeric,
             2)                                                              monthlypremium,

       partbbuydown::float / 12                                              monthlypartbbuydown,
       case when dpc.part_c_contract_id = 'N' then 'Y' else 'N' end
                                                                          as om_y_n,

       case
           when producttypeid = '1' and plantype = 'Original Medicare' then 0 -- estimatedmedoopc
           else null end                                                  as current_om_oop_medical,

       case
           when substring(dpp.plan_id, 1, 1) = 'S' then dpp.plan_id
           else null end                                                  as current_pdp,
       m_pdp.planname                                                        current_pdp_name,
       round((m_pdp.expectedpremium ::float / 12)::decimal, 2)               current_pdp_premiums,
       m_pdp.expectedpremium::float                                          current_pdp_premium_totals,
       case
           when substring(dpp.plan_id, 1, 1) in ('H', 'R') then dpp.plan_id
           else null end                                                  as current_ma,
       m_ma.planname                                                      as current_ma_plan_name,

       m_ma.ma_expectedpremium::float / 12                                   current_ma_premium,
       m_ma.ma_monthlypartbbuydown                                           current_ma_monthlypartbbuydown,
       round((m_ma.ma_expectedpremium::float / 12 - m_ma.ma_monthlypartbbuydown) ::decimal,
             2)                                                              current_ma_premiumafterbuydown,
       m_ma.ma_expectedpremium                                               current_ma_premiumtotals,
       m_ma.ma_partbbuydown                                                  current_ma_partbbuydown,
       m_ma.ma_estimatedmedoopc                                              current_ma_medicaltotals,
       m_ma.ma_estimatedrxoopc                                               current_ma_rxtotals,
       (m_ma.ma_expectedpremium ::int + m_ma.ma_estimatedmedoopc::int + m_ma.ma_estimatedrxoopc::int) -
       m_ma.ma_partbbuydown::int                                             current_ma_annualtotals,
       m_medsupp.medsupp_plantype                                            current_ms_plantype,
       m_medsupp.medsupp_planname                                            current_ms_planname,
       m_medsupp.medicareplanid                                              current_ms_medicareplanid,
       round((case
                  when (m_medsupp.medsupp_estimatedtotaloopc::float - m_medsupp.medsupp_estimatedmedoopc ::float) = 0
                      then
                      amp_current.med_supp_monthly_premium::float
                  when m_medsupp.medsupp_estimatedtotaloopc::float - m_medsupp.medsupp_estimatedmedoopc ::float > 0
                      then
                          (m_medsupp.medsupp_estimatedtotaloopc::float - m_medsupp.medsupp_estimatedmedoopc ::float) /
                          12

                  else '0.00'::float
           end)::decimal,
             2)                                                              current_ms_premium,
       round((case
                  when (m_medsupp.medsupp_estimatedtotaloopc::float - m_medsupp.medsupp_estimatedmedoopc ::float) = 0
                      then
                      amp_current.med_supp_monthly_premium::float * 12
                  when m_medsupp.medsupp_estimatedtotaloopc::float - m_medsupp.medsupp_estimatedmedoopc ::float > 0
                      then
                      (m_medsupp.medsupp_estimatedtotaloopc::float - m_medsupp.medsupp_estimatedmedoopc ::float)

                  else '0.00'::float
           end)::decimal, 2)                                                 current_ms_premiumtotals


from raw.milliman_plan_compare_recommendations mr
