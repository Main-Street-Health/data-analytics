         left join _dual_mapping dm on dpc.dual_status_code = dm.dual_level
         left join _lis_mapping lm on dpc.cost_share_code = lm.lis_level
         left join _avg_med_supp_premiums_by_plan amp on amp.med_supp_plan_type = mr.plantype
